// Métodos objeto Math

console.log(Math);

console.log(Math.PI);

const area = 7.7;

console.log(Math.round(area)); // Arredonda o número

console.log(Math.floor(area)); // Arredonda o número para baixo

console.log(Math.ceil(area)); // Arredonda o número para cima

console.log(Math.trunc(area)); // Retorna apenas a parte inteira do número


const randomNumber = Math.random(); // Gera um número decimal aleatório entre 0 e 1

console.log(randomNumber);

console.log(Math.round(randomNumber * 100)); // Gera um número aleatório de 0 a 100 arredondados